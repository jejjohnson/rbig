"""Regenerate the benchmark report at ``docs/benchmarks.md``.

Usage
-----
    python benchmarks/run_all.py            # full report
    python benchmarks/run_all.py --quick    # skip the slowest sections

The report is committed alongside the generating commit hash so a reader
can reproduce it.  Total runtime is well under 30 minutes on a laptop.
"""

from __future__ import annotations

import argparse
from pathlib import Path
import subprocess
import sys
import time

# Allow ``python benchmarks/run_all.py`` from the repo root.
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from benchmarks import (
    bench_fairness,
    bench_mi,
    bench_outliers,
    bench_preprocessing,
    bench_runtime,
    bench_sampling,
)

_OUT = Path(__file__).resolve().parent.parent / "docs" / "benchmarks.md"

# (module, is_slow) — --quick skips the slow ones.
_SECTIONS = [
    (bench_mi, False),
    (bench_outliers, False),
    (bench_sampling, True),
    (bench_preprocessing, True),
    (bench_fairness, True),
    (bench_runtime, False),
]

_HEADER = """# Benchmarks

*Auto-generated by `benchmarks/run_all.py` — do not edit by hand.*

Every table below pits an RBIG-based estimator against its standard
scikit-learn baseline on the same task. Following the project's
honest-reporting rule, **the baselines are shown on every row, including
the rows where they win**, and each section's takeaway names those cases
explicitly. Reproduce with:

```bash
python benchmarks/run_all.py
```

| Provenance | |
| --- | --- |
| Generating commit | `{commit}` |
| Wall-clock | {elapsed} |
| Mode | {mode} |
"""


def _commit_hash() -> str:
    try:
        out = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            capture_output=True,
            text=True,
            cwd=Path(__file__).resolve().parent.parent,
            check=True,
        )
        return out.stdout.strip() or "unknown"
    except Exception:
        return "unknown"


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--quick", action="store_true", help="skip slow sections")
    args = parser.parse_args()

    t0 = time.perf_counter()
    sections = []
    for module, is_slow in _SECTIONS:
        if args.quick and is_slow:
            continue
        name = module.__name__.split(".")[-1]
        print(f"[bench] running {name} ...", flush=True)
        sections.append(module.run())
    elapsed = time.perf_counter() - t0

    header = _HEADER.format(
        commit=_commit_hash(),
        elapsed=f"{elapsed:.0f} s",
        mode="quick (slow sections skipped)" if args.quick else "full",
    )
    body = "\n\n".join(s.render() for s in sections)
    _OUT.write_text(header + "\n" + body + "\n")
    print(f"[bench] wrote {_OUT} ({elapsed:.0f}s total)", flush=True)


if __name__ == "__main__":
    main()
